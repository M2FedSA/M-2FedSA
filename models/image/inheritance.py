class Adapter(nn.Module):
    def __init__(self, D_features, act_layer=nn.GELU, skip_connect=True, in_dim =64, out_dim =64):
        super().__init__()
        self.skip_connect = skip_connect
        self.act = act_layer()
        self.D_fc1 = nn.Linear(D_features, in_dim)
        self.Update_mlp = nn.Linear(in_dim, out_dim)
        self.D_fc2 = nn.Linear(out_dim, D_features)
        self.dropout1 = nn.Dropout(0.2)

    def forward(self, x):
        # x is (BT, HW+1, D)
        xs = self.D_fc1(x)
        xs = F.gelu(xs)
        xs = self.dropout1(xs)
        xs = self.Update_mlp(xs)
        xs = F.gelu(xs)
        xs = self.dropout1(xs)
        xs = self.D_fc2(xs)
        if self.skip_connect:
            x = x + xs
        else:
            x = xs
        return x

class Classifier(nn.Module):
    def __init__(self, input_dim, hidden_dim, output_dim,args):
        super().__init__()

        self.fc2 = nn.Linear(input_dim, output_dim, bias=True)
        self.bn1 = nn.BatchNorm1d(output_dim)
        nn.init.constant_(self.bn1.weight, 1)
        nn.init.constant_(self.bn1.bias, 0)
        self.bn = args.if_bn
        self.dropout1 = nn.Dropout(0.2)

        self.apply(init_weights)

    def forward(self, x):

        x = x.view(x.size(0), -1)
        if self.bn:
            x = F.gelu(self.bn1(self.fc2(x)))
        else:
            x = F.gelu(self.fc2(x))
        x = self.dropout1(x)

        return x

def init_weights(m):
    if isinstance(m, nn.Linear):
        torch.nn.init.xavier_uniform_(m.weight)
        m.bias.data.fill_(0.01)
    if isinstance(m, nn.BatchNorm1d):
        nn.init.constant_(m.weight, 1)
        nn.init.constant_(m.bias, 0)


